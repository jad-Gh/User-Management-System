    public void registerUser(AppUser user){
        appUserService.addUser(user);
        String token = UUID.randomUUID().toString();

        ConfirmationToken confirmationToken = new ConfirmationToken(
                token, LocalDateTime.now().plusMinutes(15), user
        );
        confirmationTokenService.addConfirmationToken(confirmationToken);

        //TODO send email
        String link = "http://localhost:8080/registration/confirm?token=" + token;
        emailSender.send(user.getEmail(),buildEmail(user.getFirstName(),link));

    }

    public void sendResetPasswordEmail(String email){
        AppUser user = appUserRepo.findByEmail(email)
                .orElseThrow(()->new UsernameNotFoundException("No user with such email found"));
        String token = UUID.randomUUID().toString();

        if (!user.isLocked()){
            ConfirmationToken confirmationToken = new ConfirmationToken(token,LocalDateTime.now().plusMinutes(15),user);
            confirmationTokenService.addConfirmationToken(confirmationToken);
            String link = "http://localhost:8080/registration/reset?token="+ token;
            emailSender.send(user.getEmail(),buildResetEmail(user.getFirstName(),link));
        } else {
            throw new IllegalStateException("Please verify your Email before attempting to reset password");
        }

    }

    @Transactional
    public String enableUser(String token){
        ConfirmationToken confirmationToken =  confirmationTokenService.findToken(token);
        if (confirmationToken.getExpirationDate().isBefore(LocalDateTime.now()) ){
            throw new IllegalStateException("Token is Expired");
        }
        AppUser appUser = confirmationToken.getUser();
        appUser.setLocked(false);
        confirmationTokenService.deleteToken(confirmationToken.getId());
        return "Confirmed";
    }

    @Transactional
    public String resetUserPassword(String token){
        ConfirmationToken confirmationToken = confirmationTokenService.findToken(token);
        if (confirmationToken.getExpirationDate().isBefore(LocalDateTime.now())){
            throw new IllegalStateException("Token Expired");
        }
        AppUser user = confirmationToken.getUser();
        String tempPassowrd = UUID.randomUUID().toString();
        user.setPassword(new BCryptPasswordEncoder().encode(tempPassowrd));
        confirmationTokenService.deleteToken(confirmationToken.getId());
        return "New Password is "+tempPassowrd+". Please Login and change it as soon as possible";
    }

